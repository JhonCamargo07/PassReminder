    private void jButtonAgregarPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarPassActionPerformed
        Validacion validar = new Validacion();
        String pagina = jTextFieldPagina.getText();
        String usuario = jTextFieldUsuario.getText();
        String password = jTextFieldPass.getText();
        String passwordConfirm = jTextFieldPassConfirm.getText();

        if (validar.esDatoValido("la pagina", pagina, 3, 100) && validar.esDatoValido("el usuario", usuario, 3, 100) && validar.esDatoValido("la contraseña", password, 3, 100) && validar.esDatoValido("la confimación de la contraseña", passwordConfirm, 3, 100)) {

            if (password.equals(passwordConfirm)) {

                Connection conexion = null;

                try {
                    conexion = Conexion.getConnection();
                    if (conexion.getAutoCommit()) {
                        conexion.setAutoCommit(false);
                    }

                    IPasswordDAO passwordDao = new PasswordDAO();

                    //Instanciar a un usuarioDTo para poder pasarlo cuando se haga el login
                    PasswordVO newPass = new PasswordVO(UserVO.getIdSesion(), encriptPassword(pagina), encriptPassword(usuario), encriptPassword(password));

                    //El metodo retornará un valor, lo asignamos a la variable resultado
                    passwordDao.insertar(newPass);

                    JOptionPane.showMessageDialog(null, "Contraseña agregada exitosamente");

                    jTextFieldPagina.setText("");
                    jTextFieldUsuario.setText("");
                    jTextFieldPass.setText("");
                    jTextFieldPassConfirm.setText("");
                    jTextFieldPagina.grabFocus();

                    conexion.commit();

                } catch (SQLException ex) {
                    ex.printStackTrace(System.out);
                } catch (Exception ex) {
                    Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden", "Contraseñas incorrectas", HEIGHT);
            }
        }

    }//GEN-LAST:event_jButtonAgregarPassActionPerformed

